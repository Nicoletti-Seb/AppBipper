    /**
     * Represents an asynchronous login/registration task used to authenticate
     * the user.
     */
    public class UserLoginTask extends AsyncTask<Object, Object, String> {

        private final String mEmail;
        private final String mPassword;

        UserLoginTask(String email, String password) {
            mEmail = email;
            mPassword = password;
        }

        @Override
        protected String doInBackground(Object... params) {
            String server = getResources().getString(R.string.url_server);
            String service = getResources().getString(R.string.url_service_login);
            String urlLogin = server + service;
            try {
                HttpClient client = new DefaultHttpClient();
                HttpPost post = new HttpPost(urlLogin);

                //addHeader
                post.setHeader("Content-Type", "application/json");
                Login log = new Login(mEmail, mPassword);
                Gson gson = new Gson();
                String json = gson.toJson(log);

                System.out.println("Content json : " + json);
                StringEntity entity = new StringEntity(json);

                post.setEntity(entity);

                HttpResponse response = client.execute(post);
                System.out.println("\nSending 'POST' request to URL : " + urlLogin);
                System.out.println("Post parameters : " + post.getEntity());
                System.out.println("Response Code : " +
                        response.getStatusLine().getStatusCode());

                BufferedReader rd = new BufferedReader(
                        new InputStreamReader(response.getEntity().getContent()));

                StringBuffer result = new StringBuffer();
                String line = "";
                while ((line = rd.readLine()) != null) {
                    result.append(line);
                }

                System.out.println(result.toString());
                return result.toString();

            } catch (Exception e) {
                return null;
            }
        }

        @Override
        protected void onPostExecute(final String success) {
            mAuthTask = null;
            showProgress(false);

            Gson gson = new Gson();
            JsonObject jsonObject = gson.fromJson( success, JsonObject.class);

            System.out.println("reponse content : " + jsonObject);

            if (jsonObject.get("success").getAsBoolean()) {
                Person person = gson.fromJson(jsonObject.get("user"), Person.class);
                ((BipperApplication)getApplication()).setUser(person);

                //Update firebase token
                String token = FirebaseInstanceId.getInstance().getToken();
                person.setGcmKey(token);
                new AddKey(LoginActivity.this).execute(person.getId(), token,
                        getResources().getString(R.string.api_key_firebase));

                FirebaseMessaging.getInstance().subscribeToTopic("topics");

                Intent i = new Intent(LoginActivity.this, MenuActivity.class);
                startActivity(i);
                finish();
            } else {
                mPasswordView.setError(jsonObject.get("message").toString());
                mPasswordView.requestFocus();
            }
        }

        @Override
        protected void onCancelled() {
            mAuthTask = null;
            showProgress(false);
        }
    }

    private class Login {

        private String email;

        private String password;


        public Login(String email, String password) {
            this.email = email;
            this.password = password;
        }
    }
